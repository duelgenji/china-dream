function ajaxmodifypsw (params, ok, fail, error) {
    $.ajax({
        url: "../userInfo/modifyPassword",
        type : "post",
        dataType: "json",
        data : params,
        success: function (result) {
            if (result.success == 1) {
                ok(result);
            } else {
                fail(result);
            }
        },
        error: error
        //complete
    })
}